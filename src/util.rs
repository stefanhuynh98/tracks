use rand::Rng;

pub fn random_username() -> String {
    let mut rng = rand::thread_rng();

    let r1 = (rng.gen::<f64>() * 100.0) as usize;
    let r2 = (rng.gen::<f64>() * 100.0) as usize;
    let r3 = rng.gen::<f64>();
    let adjective = ADJECTIVES[r1];
    let noun = NOUNS[r2];
    let identifier = ((r3 * 900.0 + 100.0) * 10.0) as u64;
    return format!("{}{}{}", adjective, noun, identifier);
}

